def draw_header_footer(canvas, doc):
    canvas.saveState()
    w, h = A4
    # Header bar
    canvas.setFillColor(C_ACCENT)
    canvas.rect(0, h - 18*mm, w, 18*mm, fill=1, stroke=0)
    canvas.setFont("Helvetica-Bold", 9)
    canvas.setFillColor(colors.white)
    canvas.drawString(MARGIN, h - 10*mm, "CAREERMIND AI")
    canvas.setFont("Helvetica", 8)
    canvas.setFillColor(HexColor("#A8C4E0"))
    canvas.drawString(MARGIN, h - 15*mm, "Personality Assessment Report")
    canvas.drawRightString(w - MARGIN, h - 12.5*mm, "careermind.app")
    # Footer bar
    canvas.setFillColor(C_ACCENT)
    canvas.rect(0, 0, w, 10*mm, fill=1, stroke=0)
    canvas.setFont("Helvetica", 6.5)
    canvas.setFillColor(HexColor("#A8C4E0"))
    canvas.drawCentredString(w/2, 3.5*mm,
        "Generated by CareerMind AI   |   careermind.app   |   Discover your personality, find your career path")
    canvas.setFont("Helvetica-Bold", 7)
    canvas.setFillColor(colors.white)
    canvas.drawRightString(w - MARGIN, 3.5*mm, f"Page {doc.page}")
    canvas.restoreState()


def ps(name, fontName="Helvetica", fontSize=9, textColor=None, leading=None,
       alignment=TA_LEFT, leftIndent=0, bold=False):
    if bold:
        fontName = "Helvetica-Bold"
    if textColor is None:
        textColor = C_DARK_GRAY
    if leading is None:
        leading = fontSize * 1.4
    return ParagraphStyle(name, fontName=fontName, fontSize=fontSize,
                          textColor=textColor, leading=leading,
                          alignment=alignment, leftIndent=leftIndent)


def section_bar(title, cw):
    t = Table([[Paragraph(title, ps("sh", fontName="Helvetica-Bold", fontSize=7,
                                     textColor=colors.white, leading=9))]],
              colWidths=[cw])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), C_ACCENT),
        ("TOPPADDING",    (0,0),(-1,-1), 4),
        ("BOTTOMPADDING", (0,0),(-1,-1), 4),
        ("LEFTPADDING",   (0,0),(-1,-1), 8),
    ]))
    return t


def generate_pdf(result, output_path):
    cw = PAGE_W - 2*MARGIN
    story = []

    # ── Identity block ──────────────────────────────────────────────────
    id_table = Table(
        [[[Paragraph(result["personality_type"],
                     ps("tc", fontName="Helvetica-Bold", fontSize=38,
                         textColor=C_ACCENT, leading=42))],
          [Spacer(1, 6*mm),
           Paragraph(result["personality_name"],
                     ps("tn", fontName="Helvetica-Bold", fontSize=17,
                         textColor=C_BLACK, leading=22)),
           Paragraph("MBTI Personality Type",
                     ps("tl", fontSize=7.5, textColor=C_MID_GRAY, leading=10))]]],
        colWidths=[45*mm, cw - 45*mm]
    )
    id_table.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), C_LIGHT_GRAY),
        ("VALIGN",        (0,0),(-1,-1), "MIDDLE"),
        ("LEFTPADDING",   (0,0),(-1,-1), 10),
        ("TOPPADDING",    (0,0),(-1,-1), 6),
        ("BOTTOMPADDING", (0,0),(-1,-1), 6),
        ("LINEAFTER",     (0,0),(0,0), 1, C_BORDER),
    ]))
    story += [id_table, Spacer(1,3*mm),
              Paragraph(result.get("description",""), ps("desc")),
              Spacer(1,5*mm),
              HRFlowable(width="100%", thickness=1, color=C_BORDER),
              Spacer(1,4*mm)]

    # ── Top Strengths ────────────────────────────────────────────────────
    story += [section_bar("TOP STRENGTHS", cw), Spacer(1,3*mm),
              Paragraph("   |   ".join(result.get("top_traits",[])),
                        ps("str", fontName="Helvetica-Bold", fontSize=9,
                            textColor=C_BLACK)),
              Spacer(1,2*mm),
              Paragraph("<b>Areas to Improve:</b>   " +
                        ",   ".join(result.get("improve_traits",[])),
                        ps("imp", fontSize=7.5, textColor=C_MID_GRAY)),
              Spacer(1,5*mm)]

    # ── Top Career Matches ───────────────────────────────────────────────
    story += [section_bar("TOP CAREER MATCHES", cw), Spacer(1,2*mm)]
    for i, c in enumerate(result.get("top_careers",[])):
        salary = c.get("salary","").replace("₹","").replace("Rs.","").strip()
        row1 = Table(
            [[Paragraph(f"{i+1}.", ps("n", fontName="Helvetica-Bold", fontSize=9, textColor=C_BLACK)),
              Paragraph(c.get("title",""), ps("ct", fontName="Helvetica-Bold", fontSize=10, textColor=C_BLACK)),
              Paragraph(salary, ps("sal", fontName="Helvetica-Bold", fontSize=9,
                                   textColor=C_GREEN, alignment=TA_RIGHT))]],
            colWidths=[8*mm, cw-8*mm-28*mm, 28*mm]
        )
        row1.setStyle(TableStyle([
            ("VALIGN",(0,0),(-1,-1),"TOP"),
            ("LEFTPADDING",(0,0),(-1,-1),2),
            ("TOPPADDING",(0,0),(-1,-1),0),
            ("BOTTOMPADDING",(0,0),(-1,-1),0),
        ]))
        row2 = Table(
            [[Spacer(10,1), Paragraph(c.get("reason",""), ps("cr", fontSize=8.5, textColor=C_DARK_GRAY))]],
            colWidths=[10*mm, cw-10*mm]
        )
        row2.setStyle(TableStyle([
            ("LEFTPADDING",(0,0),(-1,-1),2),
            ("TOPPADDING",(0,0),(-1,-1),0),
            ("BOTTOMPADDING",(0,0),(-1,-1),2),
        ]))
        bg = C_LIGHT_GRAY if i % 2 == 0 else C_WHITE
        block = Table([[row1],[row2]], colWidths=[cw])
        block.setStyle(TableStyle([
            ("BACKGROUND",    (0,0),(-1,-1), bg),
            ("TOPPADDING",    (0,0),(-1,-1), 5),
            ("BOTTOMPADDING", (0,0),(-1,-1), 5),
            ("LEFTPADDING",   (0,0),(-1,-1), 6),
            ("LINEBELOW",     (0,-1),(-1,-1), 0.4, C_BORDER),
        ]))
        story.append(block)
    story.append(Spacer(1,5*mm))

    # ── Recommended Courses ──────────────────────────────────────────────
    story += [section_bar("RECOMMENDED COURSES", cw), Spacer(1,2*mm)]
    hdr = [["#","Course Name","Platform","Duration","Cost","URL"]]
    rows = hdr + [[str(i+1), c.get("name",""), c.get("platform",""),
                   c.get("duration",""), "Free" if c.get("free") else "Paid",
                   c.get("url","")]
                  for i,c in enumerate(result.get("courses",[]))]
    ct = Table(rows,
               colWidths=[8*mm, 52*mm, 22*mm, 18*mm, 12*mm,
                           cw-8*mm-52*mm-22*mm-18*mm-12*mm],
               repeatRows=1)
    ct.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,0), C_ACCENT2),
        ("TEXTCOLOR",     (0,0),(-1,0), colors.white),
        ("FONTNAME",      (0,0),(-1,0), "Helvetica-Bold"),
        ("FONTSIZE",      (0,0),(-1,0), 7.5),
        ("TOPPADDING",    (0,0),(-1,0), 4),
        ("BOTTOMPADDING", (0,0),(-1,0), 4),
        ("FONTNAME",      (0,1),(-1,-1), "Helvetica"),
        ("FONTSIZE",      (0,1),(-1,-1), 8),
        ("TEXTCOLOR",     (0,1),(-1,-1), C_DARK_GRAY),
        ("ROWBACKGROUNDS",(0,1),(-1,-1), [colors.white, C_LIGHT_GRAY]),
        ("TOPPADDING",    (0,1),(-1,-1), 4),
        ("BOTTOMPADDING", (0,1),(-1,-1), 4),
        ("LEFTPADDING",   (0,0),(-1,-1), 5),
        ("RIGHTPADDING",  (0,0),(-1,-1), 5),
        ("GRID",          (0,0),(-1,-1), 0.3, C_BORDER),
        ("ALIGN",         (4,0),(4,-1), "CENTER"),
        ("TEXTCOLOR",     (4,1),(4,-1), C_GREEN),
        ("FONTNAME",      (4,1),(4,-1), "Helvetica-Bold"),
    ]))
    story += [ct, Spacer(1,5*mm)]

    # ── Skill Gap Analysis ───────────────────────────────────────────────
    story += [section_bar("SKILL GAP ANALYSIS", cw), Spacer(1,2*mm)]
    col = (cw - 4*mm) / 2
    sh = Table([["SKILLS YOU HAVE", "SKILLS TO LEARN"]], colWidths=[col, col])
    sh.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(0,0), HexColor("#D4EDDA")),
        ("BACKGROUND",    (1,0),(1,0), HexColor("#FFF3CD")),
        ("TEXTCOLOR",     (0,0),(0,0), C_GREEN),
        ("TEXTCOLOR",     (1,0),(1,0), C_ORANGE),
        ("FONTNAME",      (0,0),(-1,-1), "Helvetica-Bold"),
        ("FONTSIZE",      (0,0),(-1,-1), 8),
        ("ALIGN",         (0,0),(-1,-1), "CENTER"),
        ("TOPPADDING",    (0,0),(-1,-1), 5),
        ("BOTTOMPADDING", (0,0),(-1,-1), 5),
        ("GRID",          (0,0),(-1,-1), 0.5, C_BORDER),
    ]))
    story.append(sh)
    have  = result.get("skills_you_have", [])
    learn = result.get("skills_to_learn", [])
    mr    = max(len(have), len(learn))
    sdata = [[Paragraph(("• " + have[i])  if i < len(have)  else "", ps("sk")),
              Paragraph(("• " + learn[i]) if i < len(learn) else "", ps("sk"))]
             for i in range(mr)]
    st = Table(sdata, colWidths=[col, col])
    st.setStyle(TableStyle([
        ("ROWBACKGROUNDS",(0,0),(-1,-1), [colors.white, C_LIGHT_GRAY]),
        ("TOPPADDING",    (0,0),(-1,-1), 4),
        ("BOTTOMPADDING", (0,0),(-1,-1), 4),
        ("LEFTPADDING",   (0,0),(-1,-1), 8),
        ("GRID",          (0,0),(-1,-1), 0.3, C_BORDER),
    ]))
    story += [st, Spacer(1,5*mm)]

    # ── Learning Roadmap ─────────────────────────────────────────────────
    story += [section_bar("LEARNING ROADMAP", cw), Spacer(1,2*mm)]
    for ph in result.get("roadmap", []):
        tasks = [Paragraph(f"• {t}", ps("bt", fontSize=8.5, textColor=C_DARK_GRAY,
                                        leftIndent=10, leading=13))
                 for t in ph.get("tasks", [])]
        row = Table(
            [[[Paragraph(ph.get("month",""),
                          ps("rm", fontName="Helvetica-Bold", fontSize=9.5,
                              textColor=C_ACCENT, leading=13)),
               Paragraph(ph.get("goal",""),
                          ps("rg", fontName="Helvetica-BoldOblique", fontSize=8.5,
                              textColor=C_DARK_GRAY, leading=12))],
              tasks]],
            colWidths=[45*mm, cw - 45*mm]
        )
        row.setStyle(TableStyle([
            ("BACKGROUND",    (0,0),(0,0), HexColor("#EAF0F8")),
            ("BACKGROUND",    (1,0),(1,0), colors.white),
            ("VALIGN",        (0,0),(-1,-1), "TOP"),
            ("LEFTPADDING",   (0,0),(-1,-1), 8),
            ("TOPPADDING",    (0,0),(-1,-1), 6),
            ("BOTTOMPADDING", (0,0),(-1,-1), 6),
            ("LINEAFTER",     (0,0),(0,0), 0.5, C_BORDER),
            ("LINEBELOW",     (0,0),(-1,-1), 0.5, C_BORDER),
        ]))
        story += [KeepTogether(row), Spacer(1,1*mm)]
    story.append(Spacer(1,4*mm))

    # ── Work & Leadership Style ──────────────────────────────────────────
    story += [section_bar("WORK & LEADERSHIP STYLE", cw), Spacer(1,2*mm)]
    wdata = [
        [Paragraph("Team Style",       ps("lk", fontName="Helvetica-Bold", fontSize=8.5, textColor=C_ACCENT2)),
         Paragraph(result.get("team_style",""),        ps("wt"))],
        [Paragraph("Leadership Style", ps("lk2", fontName="Helvetica-Bold", fontSize=8.5, textColor=C_ACCENT2)),
         Paragraph(result.get("leadership_style",""),  ps("wt2"))],
        [Paragraph("Ideal Environment",ps("lk3", fontName="Helvetica-Bold", fontSize=8.5, textColor=C_ACCENT2)),
         Paragraph(result.get("ideal_work_environment",""), ps("wt3"))],
    ]
    wt = Table(wdata, colWidths=[38*mm, cw - 38*mm])
    wt.setStyle(TableStyle([
        ("ROWBACKGROUNDS",(0,0),(-1,-1), [C_LIGHT_GRAY, colors.white]),
        ("TOPPADDING",    (0,0),(-1,-1), 6),
        ("BOTTOMPADDING", (0,0),(-1,-1), 6),
        ("LEFTPADDING",   (0,0),(-1,-1), 8),
        ("RIGHTPADDING",  (0,0),(-1,-1), 8),
        ("VALIGN",        (0,0),(-1,-1), "TOP"),
        ("LINEBELOW",     (0,0),(-1,-1), 0.3, C_BORDER),
        ("LINEAFTER",     (0,0),(0,-1), 0.5, C_BORDER),
    ]))
    story += [wt, Spacer(1,5*mm)]

    # ── Famous People ────────────────────────────────────────────────────
    story += [section_bar("FAMOUS PEOPLE WITH YOUR PERSONALITY", cw), Spacer(1,3*mm)]
    fp = result.get("famous_people", [])
    fc = [[Paragraph(n, ps(f"fp{i}", fontName="Helvetica-Bold", fontSize=9,
                           textColor=C_ACCENT, alignment=TA_CENTER)) for i,n in enumerate(fp)]]
    ft = Table(fc, colWidths=[cw/len(fp)]*len(fp))
    ft.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), HexColor("#EAF0F8")),
        ("TOPPADDING",    (0,0),(-1,-1), 8),
        ("BOTTOMPADDING", (0,0),(-1,-1), 8),
        ("GRID",          (0,0),(-1,-1), 0.4, C_BORDER),
    ]))
    story.append(ft)

    # ── Build ────────────────────────────────────────────────────────────
    doc = BaseDocTemplate(
        output_path, pagesize=A4,
        leftMargin=MARGIN, rightMargin=MARGIN,
        topMargin=22*mm, bottomMargin=14*mm,
        title="CareerMind AI – Personality Assessment Report",
        author="CareerMind AI",
    )
    frame   = Frame(MARGIN, 14*mm, PAGE_W-2*MARGIN, PAGE_H-22*mm-14*mm, id="main")
    tmpl    = PageTemplate(id="main", frames=[frame], onPage=draw_header_footer)
    doc.addPageTemplates([tmpl])
    doc.build(story)


