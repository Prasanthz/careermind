def draw_header_footer(canvas, doc):
    canvas.saveState()
    w, h = A4
    # Header bar
    canvas.setFillColor(C_ACCENT)
    canvas.rect(0, h - 18*mm, w, 18*mm, fill=1, stroke=0)
    canvas.setFont("Helvetica-Bold", 9)
    canvas.setFillColor(colors.white)
    canvas.drawString(MARGIN, h - 10*mm, "CAREERMIND AI")
    canvas.setFont("Helvetica", 8)
    canvas.setFillColor(HexColor("#A8C4E0"))
    canvas.drawString(MARGIN, h - 15*mm, "Personality Assessment Report")
    canvas.drawRightString(w - MARGIN, h - 12.5*mm, "careermind.app")
    # Footer bar
    canvas.setFillColor(C_ACCENT)
    canvas.rect(0, 0, w, 10*mm, fill=1, stroke=0)
    canvas.setFont("Helvetica", 6.5)
    canvas.setFillColor(HexColor("#A8C4E0"))
    canvas.drawCentredString(w/2, 3.5*mm,
        "Generated by CareerMind AI   |   careermind.app   |   Discover your personality, find your career path")
    canvas.setFont("Helvetica-Bold", 7)
    canvas.setFillColor(colors.white)
    canvas.drawRightString(w - MARGIN, 3.5*mm, f"Page {doc.page}")
    canvas.restoreState()


def ps(name, fontName="Helvetica", fontSize=9, textColor=None, leading=None,
       alignment=TA_LEFT, leftIndent=0, bold=False):
    if bold:
        fontName = "Helvetica-Bold"
    if textColor is None:
        textColor = C_DARK_GRAY
    if leading is None:
        leading = fontSize * 1.4
    return ParagraphStyle(name, fontName=fontName, fontSize=fontSize,
                          textColor=textColor, leading=leading,
                          alignment=alignment, leftIndent=leftIndent)


def section_bar(title, cw):
    t = Table([[Paragraph(title, ps("sh", fontName="Helvetica-Bold", fontSize=7,
                                     textColor=colors.white, leading=9))]],
              colWidths=[cw])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), C_ACCENT),
        ("TOPPADDING",    (0,0),(-1,-1), 4),
        ("BOTTOMPADDING", (0,0),(-1,-1), 4),
        ("LEFTPADDING",   (0,0),(-1,-1), 8),
    ]))
    return t


