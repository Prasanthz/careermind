def wrap(draw, text, font, max_w):
    words = text.split()
    lines, line = [], []
    for word in words:
        probe = " ".join(line + [word])
        if draw.textlength(probe, font=font) <= max_w:
            line.append(word)
        else:
            if line:
                lines.append(" ".join(line))
            line = [word]
    if line:
        lines.append(" ".join(line))
    return lines
 
 
